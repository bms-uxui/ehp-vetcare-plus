  brand: string;
  description: string;
  priceBaht: number;
  originalPriceBaht?: number;
  rating: number;
  reviewCount: number;
  subscriptionEligible: boolean;
  subscriptionDiscountPct?: number; // e.g. 10 = 10% off on subscribe
  recommendedFor: ('dog' | 'cat' | 'rabbit')[];
