/** รูปหลักของสินค้า — รูปจริงในเครื่องมาก่อน URL เสมอ */
export const productImageSource = (p: Product): ImageSourcePropType | undefined =>
  p.image ?? (p.imageUrl ? { uri: p.imageUrl } : undefined);

export const getProductImages = (p: Product): ImageSourcePropType[] => {
  // สินค้าที่มีรูปถ่ายจริงใช้รูปเดียวพอ — ไม่ปนรูป stock ของหมวดที่ไม่ใช่ตัวสินค้า
  if (p.image) return [p.image];
  const list: ImageSourcePropType[] = [];
  if (p.imageUrl) list.push({ uri: p.imageUrl });
  list.push(...categoryGallery[p.category].map((uri) => ({ uri })));
  return list;
};

